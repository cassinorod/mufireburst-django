const cart = [];
let selectedProduct = null;
let activeCategory = 'all';
let visibleLimit = 40;

const productContainer = document.getElementById('shop-products');
const cartList = document.getElementById('shop-cart-list');
const cartEmpty = document.getElementById('shop-cart-empty');
const cartTotal = document.getElementById('shop-cart-total');
const cartCount = document.getElementById('shop-cart-count');
const cartDrawer = document.getElementById('shop-cart-drawer');
const modal = document.getElementById('shop-modal');
const successModal = document.getElementById('shop-success');
const loadMoreButton = document.getElementById('shop-load-more');
const productSummary = document.getElementById('shop-product-summary');

const excellentOptions = [
  'Excellent Damage Rate +10%',
  'Increase Damage +level/20',
  'Increase Damage +2%',
  'Increase attacking speed +7',
  'Increase recovery life when kill monster +life/8',
  'Increase recovery mana when kill monster +mana/8'
];

function money(value) {
  return `${value.toLocaleString('es-AR')} créditos`;
}

function getFilteredProducts() {
  return activeCategory === 'all'
    ? shopProducts
    : shopProducts.filter((product) => product.category === activeCategory);
}

function renderProducts() {
  const filteredProducts = getFilteredProducts();
  const visibleProducts = filteredProducts.slice(0, visibleLimit);

  productContainer.innerHTML = visibleProducts.map((product) => `
    <article class="shop-card" data-id="${product.id}">
      <span class="shop-card__category">${product.category}</span>
      <div class="shop-card__media shop-card__media--real">
        <img src="${product.image}" alt="${product.name}" width="220" height="220" loading="lazy" decoding="async">
      </div>
      <div class="shop-card__body">
        <h2>${product.name}</h2>
        <p>${money(product.price)}</p>
        <small>Item clasico de Mu Online</small>
        <button class="button shop-card__details" type="button" data-id="${product.id}">Ver más</button>
      </div>
    </article>
  `).join('');

  if (productSummary) {
    productSummary.textContent = `Mostrando ${visibleProducts.length} de ${filteredProducts.length} items`;
  }
  if (loadMoreButton) {
    loadMoreButton.hidden = visibleLimit >= filteredProducts.length;
  }
}

function resetConfig() {
  const level = document.getElementById('item-level');
  const life = document.getElementById('item-life');
  if (level) level.value = '0';
  if (life) life.value = '0';
  document.querySelectorAll('.shop-option-checkbox').forEach((checkbox) => { checkbox.checked = false; });
  updateExcellentLimit();
}

function updateExcellentLimit() {
  const selected = [...document.querySelectorAll('.shop-excellent')].filter((checkbox) => checkbox.checked);
  const limitReached = selected.length >= 3;
  document.querySelectorAll('.shop-excellent').forEach((checkbox) => {
    if (!checkbox.checked) checkbox.disabled = limitReached;
  });
}

function readConfig() {
  const level = document.getElementById('item-level')?.value || '0';
  const life = document.getElementById('item-life')?.value || '0';
  const luck = document.getElementById('item-luck')?.checked || false;
  const skill = document.getElementById('item-skill')?.checked || false;
  const excellent = [...document.querySelectorAll('.shop-excellent')]
    .filter((checkbox) => checkbox.checked)
    .map((checkbox) => checkbox.value)
    .slice(0, 3);
  return { level, life, luck, skill, excellent };
}

function configLabel(config) {
  const parts = [`+${config.level}`, `Life +${config.life}`];
  if (config.luck) parts.push('Luck');
  if (config.skill) parts.push('Skill');
  if (config.excellent.length) parts.push(`${config.excellent.length} opciones exe`);
  return parts.join(' · ');
}

function configKey(product, config) {
  return `${product.id}|${config.level}|${config.life}|${config.luck}|${config.skill}|${config.excellent.join(',')}`;
}

function openModal(productId) {
  selectedProduct = shopProducts.find((product) => product.id === productId);
  if (!selectedProduct) return;

  document.getElementById('shop-modal-title').textContent = selectedProduct.name;
  document.getElementById('shop-modal-img').src = selectedProduct.image;
  document.getElementById('shop-modal-img').alt = selectedProduct.name;
  document.getElementById('shop-modal-category').textContent = selectedProduct.category;
  document.getElementById('shop-modal-description').textContent = selectedProduct.description;
  document.getElementById('shop-modal-price').textContent = `Precio: ${money(selectedProduct.price)}`;
  resetConfig();

  modal.classList.add('is-open');
  modal.setAttribute('aria-hidden', 'false');
}

function closeModal() {
  modal.classList.remove('is-open');
  modal.setAttribute('aria-hidden', 'true');
}

function requireShopSession() {
  if (!window.MuFireburstAccounts) return true;
  if (MuFireburstAccounts.getCurrentAccount()) return true;

  MuFireburstAccounts.setReturnTo('pages/tienda.html');
  MuFireburstAccounts.setLoginNotice('Tu sesión finalizó o todavía no iniciaste sesión. Ingresá para comprar en la tienda.');
  window.location.href = '../index.html#inicio';
  return false;
}

function addToCart(product) {
  if (!requireShopSession()) return;
  const config = readConfig();
  const key = configKey(product, config);
  const existing = cart.find((item) => item.key === key);
  if (existing) {
    existing.quantity += 1;
  } else {
    cart.push({ ...product, key, quantity: 1, config, configText: configLabel(config) });
  }
  renderCart();
  openCart();
}

function renderCart() {
  let total = 0;
  let count = 0;

  cartList.innerHTML = cart.map((item) => {
    total += item.price * item.quantity;
    count += item.quantity;
    return `
      <li class="shop-cart-row">
        <img src="${item.image}" alt="${item.name}" width="54" height="54" loading="lazy" decoding="async">
        <div class="shop-cart-row__info">
          <strong>${item.name}</strong>
          <small>${item.configText}</small>
          <span>Precio: ${money(item.price)}</span>
        </div>
        <div class="shop-cart-row__actions">
          <button type="button" data-action="decrease" data-key="${item.key}" aria-label="Restar ${item.name}">-</button>
          <span>${item.quantity}</span>
          <button type="button" data-action="increase" data-key="${item.key}" aria-label="Sumar ${item.name}">+</button>
          <button class="shop-cart-row__remove" type="button" data-action="remove" data-key="${item.key}" aria-label="Eliminar ${item.name}">×</button>
        </div>
      </li>
    `;
  }).join('');

  cartEmpty.hidden = cart.length > 0;
  cartTotal.textContent = money(total);
  cartCount.textContent = count;
}

function openCart() {
  cartDrawer.classList.add('is-open');
  cartDrawer.setAttribute('aria-hidden', 'false');
}

function closeCart() {
  cartDrawer.classList.remove('is-open');
  cartDrawer.setAttribute('aria-hidden', 'true');
}

productContainer.addEventListener('click', (event) => {
  const button = event.target.closest('.shop-card__details');
  if (!requireShopSession()) return;
  if (button) openModal(button.dataset.id);
});

document.getElementById('shop-modal-buy').addEventListener('click', () => {
  if (selectedProduct) addToCart(selectedProduct);
  closeModal();
});

document.getElementById('shop-modal-close').addEventListener('click', closeModal);
modal.addEventListener('click', (event) => {
  if (event.target === modal) closeModal();
});

document.getElementById('shop-cart-open').addEventListener('click', () => {
  if (!requireShopSession()) return;
  openCart();
});
document.getElementById('shop-cart-close').addEventListener('click', closeCart);

document.querySelectorAll('.shop-filter').forEach((button) => {
  button.addEventListener('click', () => {
    document.querySelectorAll('.shop-filter').forEach((btn) => btn.classList.remove('is-active'));
    button.classList.add('is-active');
    activeCategory = button.dataset.category;
    visibleLimit = 40;
    renderProducts();
  });
});

if (loadMoreButton) {
  loadMoreButton.addEventListener('click', () => {
    visibleLimit += 40;
    renderProducts();
  });
}

document.querySelectorAll('.shop-option-checkbox').forEach((checkbox) => {
  checkbox.addEventListener('change', updateExcellentLimit);
});

cartList.addEventListener('click', (event) => {
  const button = event.target.closest('button[data-action]');
  if (!button) return;
  if (!requireShopSession()) return;

  const item = cart.find((product) => product.key === button.dataset.key);
  const index = cart.findIndex((product) => product.key === button.dataset.key);
  if (!item) return;

  if (button.dataset.action === 'increase') item.quantity += 1;
  if (button.dataset.action === 'decrease') {
    item.quantity -= 1;
    if (item.quantity <= 0) cart.splice(index, 1);
  }
  if (button.dataset.action === 'remove') cart.splice(index, 1);
  renderCart();
});

document.getElementById('shop-clear').addEventListener('click', () => {
  cart.length = 0;
  renderCart();
});

document.getElementById('shop-checkout').addEventListener('click', () => {
  if (!requireShopSession()) return;
  if (cart.length === 0) return;
  cart.length = 0;
  renderCart();
  closeCart();
  successModal.classList.add('is-open');
  successModal.setAttribute('aria-hidden', 'false');
});

document.getElementById('shop-success-close').addEventListener('click', () => {
  successModal.classList.remove('is-open');
  successModal.setAttribute('aria-hidden', 'true');
});

renderProducts();
renderCart();
