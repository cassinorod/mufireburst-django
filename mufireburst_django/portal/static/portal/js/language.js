const languageButtons = document.querySelectorAll('[data-lang]');

function applyLanguage(language) {
  const selectedTranslations = translations[language] || translations.es;

  document.documentElement.lang = language;

  if (selectedTranslations['seo.title']) {
    document.title = selectedTranslations['seo.title'];
  } else if (selectedTranslations['page.title']) {
    document.title = selectedTranslations['page.title'];
  }

  if (selectedTranslations['seo.description']) {
    const descriptionTargets = [
      'meta[name="description"]',
      'meta[property="og:description"]',
      'meta[name="twitter:description"]'
    ];

    descriptionTargets.forEach((selector) => {
      const meta = document.querySelector(selector);
      if (meta) {
        meta.setAttribute('content', selectedTranslations['seo.description']);
      }
    });
  }

  if (selectedTranslations['seo.title']) {
    const titleTargets = [
      'meta[property="og:title"]',
      'meta[name="twitter:title"]'
    ];

    titleTargets.forEach((selector) => {
      const meta = document.querySelector(selector);
      if (meta) {
        meta.setAttribute('content', selectedTranslations['seo.title']);
      }
    });
  }

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    if (selectedTranslations[key]) {
      element.textContent = selectedTranslations[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (selectedTranslations[key]) {
      element.setAttribute('placeholder', selectedTranslations[key]);
    }
  });

  document.querySelectorAll('[data-i18n-alt]').forEach((element) => {
    const key = element.dataset.i18nAlt;
    if (selectedTranslations[key]) {
      element.setAttribute('alt', selectedTranslations[key]);
    }
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (selectedTranslations[key]) {
      element.setAttribute('aria-label', selectedTranslations[key]);
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', isActive ? 'true' : 'false');
  });

  localStorage.setItem('mufireburst-language', language);
}

languageButtons.forEach((button) => {
  button.addEventListener('click', () => {
    applyLanguage(button.dataset.lang);
  });
});

const savedLanguage = localStorage.getItem('mufireburst-language') || 'es';
applyLanguage(savedLanguage);
